export function searchUzbekistanCities(query) {
  if (!query || query.length < 3) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  
  return UZBEKISTAN_REGIONS
    .filter(city => 
      city.name.toLowerCase().includes(normalizedQuery) ||
      city.region.toLowerCase().includes(normalizedQuery)
    )
    .slice(0, 10) // Maksimum 10 ta natija
    .map(city => ({
      name: city.name,
      fullName: `${city.name}, ${city.region}`,
      region: city.region,
      lat: city.lat,
      lng: city.lng,
      type: city.type,
      isUzbekistan: true
    }));
}